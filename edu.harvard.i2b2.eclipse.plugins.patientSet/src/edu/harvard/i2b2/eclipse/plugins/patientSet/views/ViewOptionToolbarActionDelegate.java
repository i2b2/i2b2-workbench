package edu.harvard.i2b2.eclipse.plugins.patientSet.views;

import org.eclipse.jface.action.IAction;
import org.eclipse.jface.viewers.ISelection;
import org.eclipse.ui.IViewActionDelegate;
import org.eclipse.ui.IViewPart;

import edu.harvard.i2b2.patientSet.ui.DisplayOptionsDialog;

public class ViewOptionToolbarActionDelegate implements IViewActionDelegate {

	private PatientSetView prequeryview;

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.ui.IViewActionDelegate#init(org.eclipse.ui.IViewPart)
	 */
	public void init(IViewPart view) {
		prequeryview = (PatientSetView) view;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.ui.IActionDelegate#run(org.eclipse.jface.action.IAction)
	 */
	public void run(IAction action) {
		// System.out.println("View Option Toolbar button clicked.");
		prequeryview.getViewSite().getShell().getDisplay().syncExec(
				new Runnable() {
					public void run() {
						new DisplayOptionsDialog(prequeryview).setVisible(true);
					}
				});

	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * org.eclipse.ui.IActionDelegate#selectionChanged(org.eclipse.jface.action
	 * .IAction, org.eclipse.jface.viewers.ISelection)
	 */
	public void selectionChanged(IAction action, ISelection selection) {

	}

}
